"""
Google Ads Integration Client
================================
Uses the Google Ads API Python client library.

Required environment variables:
  GOOGLE_ADS_DEVELOPER_TOKEN
  GOOGLE_ADS_CLIENT_ID
  GOOGLE_ADS_CLIENT_SECRET
  GOOGLE_ADS_REFRESH_TOKEN
  GOOGLE_ADS_LOGIN_CUSTOMER_ID   — Manager (MCC) account ID, if applicable

The client also reads from a google-ads.yaml file (auto-generated by
config/settings.py) for the google-ads library.

Docs: https://developers.google.com/google-ads/api/docs/start
"""

import logging
import os
from typing import Any

logger = logging.getLogger(__name__)


class GoogleAdsClient:
    """Google Ads API client."""

    def __init__(self) -> None:
        self._developer_token = os.getenv("GOOGLE_ADS_DEVELOPER_TOKEN", "")
        self._client_id = os.getenv("GOOGLE_ADS_CLIENT_ID", "")
        self._client_secret = os.getenv("GOOGLE_ADS_CLIENT_SECRET", "")
        self._refresh_token = os.getenv("GOOGLE_ADS_REFRESH_TOKEN", "")
        self._login_customer_id = os.getenv("GOOGLE_ADS_LOGIN_CUSTOMER_ID", "")

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _get_client(self):
        """Build and return an authenticated GoogleAdsClient."""
        from google.ads.googleads.client import GoogleAdsClient as _Client

        config = {
            "developer_token": self._developer_token,
            "client_id": self._client_id,
            "client_secret": self._client_secret,
            "refresh_token": self._refresh_token,
            "use_proto_plus": True,
        }
        if self._login_customer_id:
            config["login_customer_id"] = self._login_customer_id

        return _Client.load_from_dict(config)

    # ------------------------------------------------------------------
    # Campaigns
    # ------------------------------------------------------------------

    async def list_campaigns(
        self, customer_id: str, status_filter: str = "ALL"
    ) -> dict[str, Any]:
        """List all campaigns for a customer account."""
        try:
            import asyncio

            client = self._get_client()
            ga_service = client.get_service("GoogleAdsService")

            where_clause = (
                "" if status_filter == "ALL" else f"WHERE campaign.status = '{status_filter}'"
            )
            query = f"""
                SELECT
                    campaign.id,
                    campaign.name,
                    campaign.status,
                    campaign.advertising_channel_type,
                    campaign_budget.amount_micros
                FROM campaign
                {where_clause}
                ORDER BY campaign.id
            """

            loop = asyncio.get_event_loop()
            stream = await loop.run_in_executor(
                None,
                lambda: ga_service.search_stream(customer_id=customer_id, query=query),
            )

            campaigns = []
            for batch in stream:
                for row in batch.results:
                    campaigns.append(
                        {
                            "id": str(row.campaign.id),
                            "name": row.campaign.name,
                            "status": row.campaign.status.name,
                            "type": row.campaign.advertising_channel_type.name,
                            "daily_budget_micros": row.campaign_budget.amount_micros,
                        }
                    )

            return {"customer_id": customer_id, "campaigns": campaigns}
        except Exception as exc:
            logger.error("list_campaigns failed: %s", exc)
            return {"error": str(exc)}

    async def get_campaign_metrics(
        self,
        customer_id: str,
        campaign_id: str,
        start_date: str | None = None,
        end_date: str | None = None,
    ) -> dict[str, Any]:
        """Fetch performance metrics for a specific campaign."""
        try:
            import asyncio
            import datetime

            start = start_date or (
                datetime.date.today() - datetime.timedelta(days=30)
            ).strftime("%Y-%m-%d")
            end = end_date or datetime.date.today().strftime("%Y-%m-%d")

            client = self._get_client()
            ga_service = client.get_service("GoogleAdsService")

            query = f"""
                SELECT
                    campaign.id,
                    campaign.name,
                    metrics.impressions,
                    metrics.clicks,
                    metrics.ctr,
                    metrics.average_cpc,
                    metrics.conversions,
                    metrics.cost_micros
                FROM campaign
                WHERE campaign.id = {campaign_id}
                  AND segments.date BETWEEN '{start}' AND '{end}'
            """

            loop = asyncio.get_event_loop()
            stream = await loop.run_in_executor(
                None,
                lambda: ga_service.search_stream(customer_id=customer_id, query=query),
            )

            totals: dict[str, Any] = {
                "impressions": 0,
                "clicks": 0,
                "conversions": 0.0,
                "cost_micros": 0,
            }
            name = ""
            for batch in stream:
                for row in batch.results:
                    name = row.campaign.name
                    totals["impressions"] += row.metrics.impressions
                    totals["clicks"] += row.metrics.clicks
                    totals["conversions"] += row.metrics.conversions
                    totals["cost_micros"] += row.metrics.cost_micros

            ctr = (
                round(totals["clicks"] / totals["impressions"] * 100, 2)
                if totals["impressions"]
                else 0
            )
            cpc = (
                round(totals["cost_micros"] / totals["clicks"] / 1_000_000, 4)
                if totals["clicks"]
                else 0
            )

            return {
                "customer_id": customer_id,
                "campaign_id": campaign_id,
                "campaign_name": name,
                "start_date": start,
                "end_date": end,
                "impressions": totals["impressions"],
                "clicks": totals["clicks"],
                "ctr_pct": ctr,
                "avg_cpc_usd": cpc,
                "conversions": round(totals["conversions"], 2),
                "spend_usd": round(totals["cost_micros"] / 1_000_000, 2),
            }
        except Exception as exc:
            logger.error("get_campaign_metrics failed: %s", exc)
            return {"error": str(exc)}

    async def create_campaign(
        self,
        customer_id: str,
        name: str,
        budget_micros: int,
        campaign_type: str,
        start_date: str | None = None,
        end_date: str | None = None,
    ) -> dict[str, Any]:
        """Create a new Google Ads campaign."""
        try:
            import asyncio

            client = self._get_client()
            campaign_budget_service = client.get_service("CampaignBudgetService")
            campaign_service = client.get_service("CampaignService")

            # 1. Create budget
            budget_op = client.get_type("CampaignBudgetOperation")
            budget = budget_op.create
            budget.name = f"{name} Budget"
            budget.amount_micros = budget_micros
            budget.delivery_method = client.enums.BudgetDeliveryMethodEnum.STANDARD

            loop = asyncio.get_event_loop()
            budget_response = await loop.run_in_executor(
                None,
                lambda: campaign_budget_service.mutate_campaign_budgets(
                    customer_id=customer_id, operations=[budget_op]
                ),
            )
            budget_resource = budget_response.results[0].resource_name

            # 2. Create campaign
            campaign_op = client.get_type("CampaignOperation")
            campaign = campaign_op.create
            campaign.name = name
            campaign.advertising_channel_type = getattr(
                client.enums.AdvertisingChannelTypeEnum, campaign_type
            )
            campaign.status = client.enums.CampaignStatusEnum.PAUSED  # Start paused for safety
            campaign.campaign_budget = budget_resource

            if start_date:
                campaign.start_date = start_date.replace("-", "")
            if end_date:
                campaign.end_date = end_date.replace("-", "")

            camp_response = await loop.run_in_executor(
                None,
                lambda: campaign_service.mutate_campaigns(
                    customer_id=customer_id, operations=[campaign_op]
                ),
            )
            campaign_resource = camp_response.results[0].resource_name
            campaign_id = campaign_resource.split("/")[-1]

            logger.info("Google Ads campaign created: %s", campaign_id)
            return {
                "status": "created",
                "campaign_id": campaign_id,
                "campaign_name": name,
                "note": "Campaign created with PAUSED status. Enable it when ready.",
            }
        except Exception as exc:
            logger.error("create_campaign failed: %s", exc)
            return {"error": str(exc)}

    async def pause_campaign(
        self, customer_id: str, campaign_id: str
    ) -> dict[str, Any]:
        """Pause a running campaign."""
        try:
            import asyncio

            client = self._get_client()
            campaign_service = client.get_service("CampaignService")

            campaign_op = client.get_type("CampaignOperation")
            campaign = campaign_op.update
            campaign.resource_name = campaign_service.campaign_path(
                customer_id, campaign_id
            )
            campaign.status = client.enums.CampaignStatusEnum.PAUSED

            field_mask = client.get_type("FieldMask")
            field_mask.paths.append("status")
            campaign_op.update_mask.CopyFrom(field_mask)

            loop = asyncio.get_event_loop()
            await loop.run_in_executor(
                None,
                lambda: campaign_service.mutate_campaigns(
                    customer_id=customer_id, operations=[campaign_op]
                ),
            )

            logger.info("Campaign %s paused for customer %s", campaign_id, customer_id)
            return {
                "status": "paused",
                "campaign_id": campaign_id,
                "customer_id": customer_id,
            }
        except Exception as exc:
            logger.error("pause_campaign failed: %s", exc)
            return {"error": str(exc)}
